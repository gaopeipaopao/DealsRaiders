public class CommentListTool {

    int ima;
    String one;
    String two;
    String three;

    public CommentListTool(int ima,String one,String two,String three){
        this.ima = ima;
        this.one = one;
        this.two = two;
        this.three = three;
    }

    public void setIma(int ima) {
        this.ima = ima;
    }

    public int getIma() {
        return ima;
    }

    public void setOne(String one) {
        this.one = one;
    }

    public String getOne() {
        return one;
    }

    public void setTwo(String two) {
        this.two = two;
    }

    public String getTwo() {
        return two;
    }

    public void setThree(String three) {
        this.three = three;
    }

    public String getThree() {
        return three;
    }
}
